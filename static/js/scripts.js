function escapeHtml(text) {
    if (!text) return '';
    // 1. Сначала экранируем всё (превращаем < в &lt;)
    let safe = text
        .replace(/&/g, "&amp;")
        .replace(/</g, "&lt;")
        .replace(/>/g, "&gt;")
        .replace(/"/g, "&quot;")
        .replace(/'/g, "&#039;");

    // 2. Разрешаем конкретные теги (превращаем &lt;br&gt; обратно в <br>)
    // регулярка ищет &lt;br&gt; с возможными пробелами внутри

    // Разрешаем <br>, <br/>, <br />
    safe = safe.replace(/&lt;br\s*\/?\s*&gt;/gi, '<br>');

    // Опционально: если в базе будет курсив <i> или жирный <b>:
    // safe = safe.replace(/&lt;b\s*&gt;/gi, '<b>');
    // safe = safe.replace(/&lt;\/b\s*&gt;/gi, '</b>');
    // safe = safe.replace(/&lt;i\s*&gt;/gi, '<i>');
    // safe = safe.replace(/&lt;\/i\s*&gt;/gi, '</i>');

    return safe;
}


$(document).ready(function () {

    // Валидация ввода (только кириллица)
    $('#word').on('input', function () {
        const regex = /[а-яА-ЯёЁ ]/;
        let value = $(this).val().toLowerCase();
        $(this).val(value);
        if (value.length > 0 && !regex.test(value.slice(-1))) {
            $('#warning').show();
            $(this).val(value.slice(0, -1));
        } else {
            $('#warning').hide();
        }
    });

});